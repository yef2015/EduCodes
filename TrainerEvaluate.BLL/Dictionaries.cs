        /// <summary>
        /// 获得数据列表
        /// </summary>
        public List<Models.Dictionaries> DataTableToList(DataTable dt)
        {
            List<Models.Dictionaries> modelList = new List<Models.Dictionaries>();
            int rowsCount = dt.Rows.Count;
            if (rowsCount > 0)
            {
                Models.Dictionaries model;
                for (int n = 0; n < rowsCount; n++)
                {
                    model = dal.DataRowToModel(dt.Rows[n]);
                    if (model != null)
                    {
                        modelList.Add(model);
                    }
                }
            }
            return modelList;
        }

